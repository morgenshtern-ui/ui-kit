type IRefPalette<Base extends typeof RefPaletteBase = typeof RefPaletteBase> = {
  [Name in keyof Base]: {
    [Val in keyof Base[Name]]: {
      value: Base[Name][Val]
      name: Name extends string ? (Val extends string ? `${Name}-${Val}` : never) : never
    }
  }
}

export const RefPalette = Object.entries(RefPaletteBase).reduce(
  (result, [colorName, colorMap]) => {
    result[colorName] = Object.entries(colorMap).reduce(
      (newColorMap, [colorSize, colorVal]) => {
        newColorMap[colorSize] = {
          value: colorVal,
          name: `${colorName}-${colorSize}`,
        }

        return newColorMap
      },
      {} as Record<string, { value: string; name: string }>,
    )

    return result
  },
  {} as Record<string, Record<string, { value: string; name: string }>>,
) as IRefPalette
